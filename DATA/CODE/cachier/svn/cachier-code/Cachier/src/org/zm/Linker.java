package org.zm;

import java.io.File;

public interface Linker extends SystemComponent {

    /**
     * Creates a hard link from source to destination
     * 
     * source -> destination
     * 
     * @param source
     *            Path to link start point
     * @param destination
     *            Path to target end point
     * @return link was created successfully
     */
    public boolean link(File source, File destination);

}
